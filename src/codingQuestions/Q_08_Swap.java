package codingQuestions;

public class Q_08_Swap {

    public static void main(String[] args) {
        //Swap Number

        int a = 10;
        int b = 20;
        int c;

        //1 way
        System.out.println(a);
        System.out.println(b);
        c = a;
        a = b;
        b = c;
        System.out.println(a);
        System.out.println(b);

    }
}
