package softwaretestinginterview;

public class Soru04 {

	public static void main(String[] args) {
		
		/*
		 Q #3) Write a Java Program to swap two numbers using the third variable.
		 */

		int a = 10;
		int b = 5;
		
		a = a+b;
		b = a-b;
		a = a-b;
		
		System.out.println(a + " " + b);
		//a=10 b=5 
		
	}

}
